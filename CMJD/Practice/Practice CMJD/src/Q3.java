public class Q3 {
    public static void main(String args[]){
        char ch = 'A';
        System.out.println(ch); // prints A

        ch = 66;
        System.out.println(ch); // prints B

        System.out.println('A' + 'B'); // prints 131
        System.out.println("A" + 'B'); // prints AB

    }

}
