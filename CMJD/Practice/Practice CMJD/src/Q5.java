//Increment and Decrement
public class Q5 {
    public static void main(String[] args) {
        int x = 100;
        System.out.println(x); // prints 100

        // Incrementer
        x++; // Post
        System.out.println(x); // prints 101

        ++x; // Pre
        System.out.println(x); // prints 102

        // Decrementer
        x--; // Post
        System.out.println(x); // prints 101

        --x; // Pre
        System.out.println(x); // prints 100
    }
}
