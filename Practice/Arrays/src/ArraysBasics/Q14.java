package ArraysBasics;

// Array Union

public class Q14 {
    public static void main(String[] args) {
        int[] a={1,2,3,4,5,6,7};
        int[] b={1,3,7,8,9,10,11,5,6,12};

        for (int i=0;i<a.length;i++){
            System.out.print(a[i]+", ");
        }
        for (int j=0;j<b.length;j++){
            System.out.print(b[j]+", ");
        }
    }
}
